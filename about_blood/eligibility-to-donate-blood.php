                    <h3 style="color:green;">Eligibility to Donate Blood</h3>
                    
					<h4 style="color:blue;">Basic Requirements</h4>
						<p class="text-justify">•	Be at least 17 years old in most states
							<br>•	Weight: At least 45 kg (100 Ibs) for both males and females
							<br>•	Well Being: Feeling well that day. Not having colds, coughs or flu in the last one week. No fever (Temperature >37.5°C) in the last 3 weeks.
						</p>
					
					<h4 style="color:blue;">Major Illness/Surgery</h4>
						<p class="text-justify">Persons with the following conditions are <b>not eligible</b> for blood donation:
							<br><br>•	Diseases of the heart or lungs (Donors who are asthmatic and without symptoms of asthma is eligible)
							<br>•	High blood pressure on medication
							<br>•	Diabetes on medication
							<br>•	Major surgery (can donate after 12 months)
							<br>•	AIDS or symptoms of AIDS, such as unexplained fevers, severe night sweats, unexpected weight loss, swollen glands, chronic diarrhoea or rare cancer
							<br>•	Epilepsy
							<br>•	Hepatitis B or C
							<br>•	Syphilis
						</p>
					
					<h4 style="color:blue;">Some Common Reasons For Temporary Deferral</h4>	
						<p class="text-justify">•	Cold, flu or sore throat, please wait one week after recovery or treatment
							<br>•	Tooth extraction or dental work, please wait 3 days after treatment
							<br>•	Skin infections (minor), wait 1 week after complete healing
							<br>•	Normal pregnancy, please wait 6 weeks after delivery and when you are not breast-feeding.
							<br>•	Travelled to a malaria endemic area such as rural areas in Malaysia, Thailand, Indonesia, Philippines, India, etc, please wait 6 weeks to 3 years and ask the attending doctor/nurse whether you are eligible for donation
							<br>•	Close contact with Hepatitis B, wait 12 months and after full course of hepatitis B vaccination (and shown a satisfactory antibody response)
							<br>•	Infectious Diseases e.g. Chickenpox, Measles, Dengue, wait 6 months after recovery
						</p>
					
					<h4 style="color:blue;">Medication</h4>	
						<p class="text-justify">People who are taking drugs for cancer treatment, heart diseases, high blood pressure, diabetes or current infections will not be accepted as blood donors.
							<br>For those taking:
							<br>Traditional Medication  <i>Please wait 3 days<i><br>
							<br>Antibiotics	<i>Please wait 1 week</i><br>
							<br>Anti-malaria Medication	 <i>Please wait 4 weeks</i>
						</p>
					
