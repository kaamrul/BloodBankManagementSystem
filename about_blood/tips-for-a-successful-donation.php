                    <h3 style="color:green;">Tips for a Successful Donation</h3>
					<p class="text-justify">By following a few recommendations before, during and after your blood donation can help you make your donation experience as safe, successful and pleasant as possible.</p>
                    
					<h4 style="color:blue;">Before Your Donation</h4>
						<p class="text-justify">•	Maintain a healthy iron level in your diet by eating iron rich foods, such as spinach, red meat, fish, poultry, beans, iron-fortified cereals and raisins.
							<br>•	Get a good night's sleep.
							<br>•	Drink an extra 16 oz. of water and fluids before the donation.
							<br>•	Eat a healthy meal before your donation. Avoid fatty foods, such as hamburgers, fries or ice cream before donating. Tests for infections done on all donated blood can be affected by fats that appear in your blood for several hours after eating fatty foods.
						</p>
					
					<h4 style="color:blue;">During Your Donation</h4>
						<p class="text-justify">•	Wear clothing with sleeves that can be raised above the elbow.
							<br>•	Let the phlebotomist know if you have a preferred arm and show the staff any good veins that have been used successfully in the past to draw blood.
							<br>•	Relax, listen to music, talk to other donors or read during the donation process.
							<br>•	Take the time to enjoy a snack and a drink in the refreshments area immediately after donating.
						</p>
						
					<h4 style="color:blue;">After Your Donation</h4>	
						<p class="text-justify">•	Drink plenty of fluids over the next 24-48 hours to replenish any fluids you lost during donation.
							<br>•	Avoid strenuous physical activity or heavy lifting for about five hours after donation.
							<br>•	If you feel light headed, lie down, preferably with feet elevated, until the feeling passes.
							<br>•	In rare cases when bleeding occurs after removing the bandage, apply pressure to the site and raise your arm for 3-5 minutes. If bleeding or bruising occurs under the skin, apply a cold pack to the area periodically during the first 24 hours.
							<br>•	If for any reason something doesn't feel right, then call a doctor.
							<br>•	Enjoy the good feeling that comes with knowing that you may have saved some others live.
						</p>
					
					
